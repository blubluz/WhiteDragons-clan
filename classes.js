// Helper function to get class by name (for backward compatibility)
function getClassByName(className) {
    for (const [id, classData] of Object.entries(LINEAGE2_CLASSES)) {
        if (classData.name.toLowerCase() === className.toLowerCase()) {
            return parseInt(id);
        }
    }
    return null;
}

// Helper function to get class data by ID
function getClassById(classId) {
    return LINEAGE2_CLASSES[classId] || null;
}

// Helper function to get class icon by ID (strict 5-icon scheme)
function getClassIconById(classId) {
    // Dagger classes
    const daggerClassIds = [13, 18, 45, 64, 65, 234, 215, 206, 233, 80, 79, 85, 43, 44, 29, 59, 55, 74, 43, 44, 71, 80, 85, 13, 64, 65, 234, 215, 206, 233];
    // Buffer classes
    const bufferClassIds = [21, 22, 23, 69, 70, 231, 217, 218, 219, 242, 87, 88, 89, 205, 24, 220, 25, 221, 26, 222, 27, 223, 47, 228, 48, 229, 49, 230, 50, 231];
    // Swordsinger/Bladedancer
    const swordDancerClassIds = [43, 44, 225, 226, 36, 58, 83];
    // Bounty Hunter/Warsmith
    const bountyWarsmithClassIds = [243, 244, 102, 103, 4, 5, 82];
    // Tyrant (and default)
    const tyrantClassIds = [84, 239, 63];

    if (daggerClassIds.includes(classId)) {
        return 'assets/icons/dagger.png';
    }
    if (bufferClassIds.includes(classId)) {
        return 'assets/icons/buffer.png';
    }
    if (swordDancerClassIds.includes(classId)) {
        return 'assets/icons/sword-dancer.png';
    }
    if (bountyWarsmithClassIds.includes(classId)) {
        return 'assets/icons/bounty-warsmith.png';
    }
    // Default to tyrant icon for all others
    return 'assets/icons/tyrant.png';
}

